# == Igualdad
username = 'Jon'
acceso = 'Welcome' if username == 'Jon' else 'Not welcome'
print(acceso)

# != Desigualdad
username = 'Jon'
acceso = 'Not welcome' if username != 'Jon' else 'Welcome'
print(acceso)

# >= Mayor o igual que
age = 18
acceso = 'Welcome' if age >= 18 else 'Not welcome'
print(acceso)

# <= Menor o igual que
age = 17
acceso = 'Not Welcome' if age <= 17 else 'Welcome'
print(acceso)